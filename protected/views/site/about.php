<h3 class="worker">«КЭР-Инжиниринг» группа компаний</h3>
<div class="priveet">
    <!--    <div class="priveet_foto"></div>-->
    <div class="priveet_text">
        <img src="../../../i/priveet.jpg" />
        «КЭР-Инжиниринг» является ведущей инженерной компанией России.
        Численность персонала составляет около 1000 человек. Потенциал компании основан на специалистах, которые имеют за плечами 20-25 лет трудового стажа в области инжиниринга. 7 специалистов имеют звание кандидатов технических наук. Среди деловых партнёров — компании из России, США, Германии, Франции, Китая, Украины, Беларуси. Выполнено около сотни проектов, большинство — на стратегических объектах: ТЭЦ, ГРЭС, ГЭС и т.д. «КЭР-Инжиниринг» предоставляет полный комплекс услуг по разработке и внедрению автоматизированных систем управления технологическими процессами (АСУ ТП): обследование объекта, разработка проектной документации и инженерного решения, монтаж, пуско-наладка, сервисное обслуживание). Имеет собственное производство инновационной продукции, включая: датчики давления нового поколения, а также уникальные программно-технические комплексы (АСУ ТП). Разработки компании пользуются спросом не только в России, но и в странах ближнего и дальнего зарубежья. «КЭР-Инжиниринг» занимает прочные позиции на рынке инженерных услуг и расширяет круг своих клиентов. 26 мая 2009 г. в рамках работы конференции «Вопросы текущего состоя-ния и пути дальнейшего развития АСУ ТП на ТЭС ОАО „Генерирующая компания“ на кафедре АТПП состоялось открытие учебно-лабораторной базы целевой подготовки кадров «КГЭУ – SIEMENS – КЭР ИНЖИНИРИНГ».
    </div>
    <div class="priveet_text">
        Идея создания данного учебного центра возникла в ноябре 2008 года во время встречи представителей инженерной компании «КЭР-Инжиниринг», специализирующейся на реализации крупных проектов в энергетике, с сотрудниками и студенческой научно-технической группой кафедры АТПП.

        Учебно-лабораторная база позволит не только повысить качество подготовки выпускников кафедр АТПП и ЭПА, но и проводить целевую подготовку кадров для энергетических и инжиниринговых предприятий Республики Татарстан.
