<h1 class="atppzag">Кафедра Автоматизации технологических процессов и производств (АТПП)</h1>
<div class="block1">
    Кафедра открыта в 2000 г. на базе кафедры промышленной теплоэнергетики (ПТЭ) для подготовки инженеров по автоматизации в энергетике. Необходимость в такой кафедре очевидна: нельзя стать современным высококвалифицированным техническим специалистом без знания теории автоматического управления и принципов автоматизации.
